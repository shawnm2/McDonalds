    <h1>McRaise?</h1>
    <h2>Introduction</h2>
    <p>McDonald&rsquo;s:  </p>
    <p>- is a highly profitable multi-national corporation </p>
    <p>- produces 28 billion dollars in revenue yearly  </p>
    <p>- is one of the largest minimum wage employers</p>
    <p>- has come under political pressure to raise its minimum wage to $15 dollars per hour </p>
    <p>Many critics of the movement think this will cripple the company and kill the very jobs that they are trying to enhance  </p>
    <p>The supporters of the movement cite massive corporate profits and exorbitant executive compensation as evidence that corporations such as McDonald&rsquo;s can afford such a raise and 
    it will have a minimal impact to the bottom line  </p>
    <p>The goal of this project is to explore this topic visually for the viewer to draw their own conclusion.  For the purposes of this exercise it is assumed an average 
    worker makes $8.90 per hour.</p>
